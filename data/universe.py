# HELPER FUNCTIONS
# ============================================================

def all_unique_tickers() -> List[str]:
    """Return sorted list of all 910 unique tickers across the universe."""
    tickers = set()
    for baskets in CATEGORIES.values():
        for tl in baskets.values():
            tickers.update(tl)
    return sorted(tickers)

def baskets_by_type(btype: str) -> List[str]:
    """Return list of baskets matching CORE / TAG / COUNTRY."""
    out = []
    for cat, baskets in CATEGORIES.items():
        if CATEGORY_TYPE[cat] == btype:
            out.extend(baskets.keys())
    return out

def primary_basket(ticker: str) -> str:
    """Return the primary basket for a ticker, or UNKNOWN."""
    return PRIMARY_BASKETS.get(ticker, "UNKNOWN")

def tags_for(ticker: str) -> List[str]:
    """Return list of overlay baskets where ticker appears (not primary)."""
    return TAGS.get(ticker, [])

def constituents(basket: str) -> List[str]:
    """Return list of tickers where basket is PRIMARY."""
    return PRIMARY_BASKET_CONSTITUENTS.get(basket, [])

def tickers_tagged(basket: str) -> List[str]:
    """Return all tickers tagged with basket (primary OR tag overlay)."""
    out = set()
    for ticker, primary in PRIMARY_BASKETS.items():
        if primary == basket:
            out.add(ticker)
    for ticker, tag_list in TAGS.items():
        if basket in tag_list:
            out.add(ticker)
    return sorted(out)

def category_of(basket: str) -> str:
    """Return the category containing basket."""
    for cat, baskets in CATEGORIES.items():
        if basket in baskets:
            return cat
    return "UNKNOWN"

def basket_type(basket: str) -> str:
    """Return CORE / TAG / COUNTRY for a basket."""
    cat = category_of(basket)
    return CATEGORY_TYPE.get(cat, "UNKNOWN")


